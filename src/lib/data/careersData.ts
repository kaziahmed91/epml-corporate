// Function to get job by ID
export const getJobById = (id: string) => {
  return careers.find(job => job.id === id);
};

// Function to filter jobs by department
export const getJobsByDepartment = (department: string) => {
  if (department === 'all') {
    return careers;
  }
  return careers.filter(job => job.department.toLowerCase() === department.toLowerCase());
};